	public static int[][] findeWegDurchSpielfeld(int[][] spielfeld) {
		// Deklaration von Hilfsvariablen zur Positionsbestimmung der Spielfigur
		int i = 0;
		int j = 0;
		int k = 0;

		int laenge = spielfeld.length - 1;
		int breite = spielfeld[0].length - 1;

		while (spielfeld[laenge][breite] != 2) {
			spielfeld[i][j] = 2; // Markiert den Weg der Spielfigur
			// Prüft, ob die Spielfigur "unten" angekommen ist
			if (i < laenge) {
				if (spielfeld[i + 1][j] == 0) {
					i++;
				} else {
					if (j == k)
						k = 0;
					while (spielfeld[i + 1][k] != 0 && k < breite + 1) {
						k++;
					}
					if (k > j) {
						if (spielfeld[i][j + 1] == 0)
							j++;
					}
					if (k < j) {
						if (spielfeld[i][j - 1] == 0)
							j--;
					}

				}

			} else {
				if (j < breite) {
					if (spielfeld[i][j + 1] == 0) {
						j++;
					}
				}
			}
		}
		return spielfeld;
	}
